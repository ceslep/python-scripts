import turtle

t=turtle.Turtle()
t.speed(0)
turtle.bgcolor("black")
t.pensize(2)
turtle.title("Turtle Spiral")
turtle.setup(800, 600)
t.color("red")
t.penup()
t.goto(-400,0)
t.pendown()
t.goto(400,0)
t.penup()
t.goto(0,300)
t.pendown()
t.goto(0,-300)
t.penup()
t.color("cyan")
t.goto(50,50)
t.pendown()
t.goto(200,-50)
t.goto(150,150)
t.goto(50,50)
t.hideturtle()
turtle.done()